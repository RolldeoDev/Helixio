interface AchievementCardProps {
  achievement: AchievementWithProgress;
}

function formatDate(dateStr: string | null): string {
  if (!dateStr) return '';
  return new Date(dateStr).toLocaleDateString('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
  });
}

export function AchievementCard({ achievement }: AchievementCardProps) {
  const {
    name,
    description,
    stars,
    iconName,
    isUnlocked,
    progress,
    unlockedAt,
  } = achievement;

  const iconPath = ICON_PATHS[iconName] || ICON_PATHS.book;

  return (
    <div
      className={`achievement-card ${
        isUnlocked ? 'achievement-card--unlocked' : 'achievement-card--locked'
      } achievement-card--stars-${stars}`}
      title={`${name}: ${description}`}
    >
      {/* Header with Icon and Status */}
      <div className="achievement-card__header">
        {/* Status badge */}
        <div
          className={`achievement-card__status ${
            isUnlocked
              ? 'achievement-card__status--unlocked'
              : 'achievement-card__status--locked'
          }`}
        >
          {isUnlocked ? <Check size={14} /> : <Lock size={14} />}
        </div>

        {/* Icon */}
        <div className="achievement-card__icon-wrapper">
          <div className="achievement-card__icon-bg" />
          <div className="achievement-card__icon-inner">
            <span className="achievement-card__icon">
              <svg
                viewBox="0 0 24 24"
                fill="none"
                stroke="currentColor"
                strokeWidth="1.5"
                strokeLinecap="round"
                strokeLinejoin="round"
              >
                {iconPath}
              </svg>
            </span>
          </div>
        </div>

        {/* Stars */}
        <div className="achievement-card__stars">
          {Array.from({ length: 5 }, (_, i) => (
            <Star
              key={i}
              className={`achievement-card__star ${
                i < stars
                  ? 'achievement-card__star--filled'
                  : 'achievement-card__star--empty'
              }`}
              fill={i < stars ? 'currentColor' : 'none'}
            />
          ))}
        </div>
      </div>

      {/* Body */}
      <div className="achievement-card__body">
        <h3 className="achievement-card__name">{name}</h3>
        <p className="achievement-card__description">{description}</p>

        {/* Progress bar for locked achievements with progress */}
        {!isUnlocked && progress > 0 && (
          <div className="achievement-card__progress-section">
            <div className="achievement-card__progress-bar">
              <div
                className="achievement-card__progress-fill"
                style={{ width: `${Math.min(100, progress)}%` }}
              />
            </div>
            <div className="achievement-card__progress-text">
              <span>Progress</span>
              <span className="achievement-card__progress-percent">
                {Math.round(progress)}%
              </span>
            </div>
          </div>
        )}

        {/* Unlock date for unlocked achievements */}
        {isUnlocked && unlockedAt && (
          <div className="achievement-card__unlocked-date">
            <Calendar size={12} />
            <span>Unlocked {formatDate(unlockedAt)}</span>
          </div>
        )}
      </div>
    </div>
  );
}
